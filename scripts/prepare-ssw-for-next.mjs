/**
 * Reads generated/ssw-html (base64 already replaced), strips nav/footer,
 * fixes internal links, removes Analogue Room references, writes lib/ssw/prepared/*.ts
 */
import fs from "node:fs"
import path from "node:path"

const root = process.cwd()
const srcDir = path.join(root, "generated", "ssw-html")
const outDir = path.join(root, "lib", "ssw", "prepared")

fs.mkdirSync(outDir, { recursive: true })

function stripNavFooter(html) {
  let s = html.replace(/<nav[\s\S]*?<\/nav>/i, "")
  s = s.replace(/<footer[\s\S]*?<\/footer>/i, "")
  return s.trim()
}

function fixLinks(html) {
  return html
    .replace(/href="index\.html"/g, 'href="/"')
    .replace(/href='index\.html'/g, "href='/'")
    .replace(/href="about\.html"/g, 'href="/#about"')
    .replace(/href="winery\.html"/g, 'href="/winery"')
    .replace(/href="events\.html"/g, 'href="/#events"')
    .replace(/href="private-events\.html"/g, 'href="/private-events"')
    .replace(/href="contact\.html"/g, 'href="/contact"')
    .replace(/href="gallery\.html"/g, 'href="/#gallery"')
    .replace(/href="equipment\.html"/g, 'href="/winery#facility"')
    .replace(/href="index\.html#events"/g, 'href="/#events"')
    .replace(/onclick="window\.location\.href='events\.html'"/g, 'onclick="window.location.href=\'/#events\'"')
    .replace(
      /<li>\s*<a[^>]*analogueroom[^>]*>[\s\S]*?<\/a>\s*<\/li>/gi,
      "",
    )
    .replace(/<div class="footer-sister">[\s\S]*?<\/div>/gi, "")
}

/** Fix broken extra closing divs in home events section (template bug). */
function fixHomeEvents(html) {
  if (!html.includes("events-layout")) return html
  return html.replace(
    /(<div class="events-layout">[\s\S]*?<\/div>)\s*<\/div>\s*<\/div>\s*<div class="events-cta">/,
    "$1\n  <div class=\"events-cta\">",
  )
}

const map = {
  home: "standingsunwines_v19.html",
  winery: "winery.html",
  privateEvents: "private-events.html",
  contact: "contact.html",
}

for (const [key, file] of Object.entries(map)) {
  const raw = fs.readFileSync(path.join(srcDir, file), "utf8")
  let body = stripNavFooter(raw)
  const inner = body.match(/<body[^>]*>([\s\S]*)<\/body>/i)
  const content = inner ? inner[1].trim() : body
  let html = fixLinks(content)
  if (key === "home") html = fixHomeEvents(html)
  const escaped = JSON.stringify(html)
  fs.writeFileSync(
    path.join(outDir, `${key}.ts`),
    `/** Auto-generated by scripts/prepare-ssw-for-next.mjs — do not edit by hand */\nexport const html = ${escaped} as string\n`,
    "utf8",
  )
  console.log(key, "chars", html.length)
}

console.log("Wrote lib/ssw/prepared/*.ts")
